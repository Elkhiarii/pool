int	ft_atoi(char *str)
{
	int	i;
	int	n;
	int	m;

	i = 0;
	m = 1;
	n = 0;
	while (str[i] == '\t' || str[i] == ' '
		|| str[i] == '\f' || str[i] == '\r'
		|| str[i] == '\n' || str[i] == '\v')
		i++;
	while (str[i] == '+' || str[i] == '-')
	{
		if (str[i] == '-')
			m *= -1;
		i++;
	}
	while (str[i] >= '0' && str[i] <= '9')
	{
		n = (n * 10) + (str[i] - '0');
		i++;
	}
	n *= m;
	return (n);
}
